package net.lizhaoweb.common.util.exception;

/**
 * <h1>异常 - 空文件</h1>
 *
 * @author <a href="http://www.lizhaoweb.cn">李召(John.Lee)</a>
 * @version 1.0.0.0.1
 * @notes Created on 2016年11月13日<br>
 * Revision of last commit:$Revision$<br>
 * Author of last commit:$Author$<br>
 * Date of last commit:$Date$<br>
 */
public class EmptyFileException extends IllegalArgumentException {

    private static final long serialVersionUID = 1L;

    public EmptyFileException() {
        this("The supplied file was empty (zero bytes long)");
    }

    public EmptyFileException(String s) {
        super(s);
    }

    public EmptyFileException(String message, Throwable cause) {
        super(message, cause);
    }

    public EmptyFileException(Throwable cause) {
        super(cause);
    }
}
